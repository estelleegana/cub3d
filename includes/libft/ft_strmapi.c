#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		len;
	int		i;
	char	*result;

	i = 0;
	if (!s)
		return (0);
	len = ft_strlen((char *) s);
	result = malloc((len +1) * sizeof(char));
	if (result == NULL)
		return (0);
	while (s[i])
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
